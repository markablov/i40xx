export default function getInstructionLength(mnemonic) {
  const m = mnemonic.match(/^\s*(?:\w+:)?\s*(\w+)(?:\s+|$)/);
  return m ? (instructionLengths[m[1].toLowerCase()] || 0) : 0;
}
